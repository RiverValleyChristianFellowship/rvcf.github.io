<h2>Baptism</h2>
<span class="lead">What about baptism?</span>
        The word "baptism" is the English form of the Greek word <i>baptizo</i>, which means to submerse or immerse.  All converted people in New Testament times were baptized before taking their place in a local church (see <a href="http://www.biblegateway.com/cgi-bin/bible?passage=Acts+2:41;8:12&version=NKJV" target="_blank" class="plainlink">Acts 2:41; 8:12</a>).  Christ had commanded that this should be done (<a href="http://www.biblegateway.com/cgi-bin/bible?passage=Matt+28:19&version=NKJV" target="_blank" class="plainlink">Mt. 28:19</a>).  The same pattern is observed by scriptural assemblies today.  Converts should be taught that baptism is the symbol of their being buried with Christ, and rising (as He rose) to walk "<span class="bible">in newness of life</span>" (<a href="http://www.biblegateway.com/cgi-bin/bible?passage=Rom+6:11&version=NKJV" target="_blank" class="plainlink">Rom.6:1-11</a>).


