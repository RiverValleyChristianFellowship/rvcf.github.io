<p> 8/16/12 (Nathan Hanson) Seeing David was very sobering on a whole new level to say the least. We praise the Lord for some amazing things that have happened, but still have many times wondered what God has in store. We don't quit hoping and praying for the Lord's will in this and do our best to trust Him.
We often wonder, what would David say if he were conscious right now? I know David better than most, and have recently spoken with his co-workers, many family members and friends. I don't think anyone who knows him would disagree. He has already said it. 
The words of Rachel (David's wife as of June 16th 2012) sum it up well: "We are reminded of our human frailty and how our lives are held in a fragile balance. If David could have one wish out of this whole things, I believe with all my heart that it would be that souls would bow their hearts before the Creator and Saviour of the World and accept his propitiative work on the cross for their sin and make Him Lord of their lives."
This recording is one example which seems to fit the circumstances well. It has become very popular among the guys David works with and many others. There are many copies of this being distributed every day by all those who know David. It was recorded only a few years ago by David himself. <a href="http://www.rivervalleyfellowship.org/audio/willyoubeready.wma" target="_blank" >Will You Be Ready When Your Time Comes?</a> -David Hanson</p>

To order a FREE copy of the CD please click here: <a href="http://www.rivervalleyfellowship.org/lifelines.php" target="_blank" >Life Lines Page</a>
