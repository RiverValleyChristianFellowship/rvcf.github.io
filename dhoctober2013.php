<p>10/11/13 (Rachel Hanson)
We finally have a great speech therapist! She is a gift from the Lord. She runs a ministry for autistic children on a ranch and drives an hour one-way to see David 3 times a week. She is getting us hooked up with with some augmentative speech devices but still working on verbal communication. A few days ago, David was able to say 15 words in a row audibly! It's not consistent yet but he's improving in oromotor strength as well. He's improved from being able to scan and select between 2 objects to 12 objects in 3 weeks! I'm trying to get David two real meals a day and supplement with tube feeding but it's hard to get it in with so many doctor's appointments, therapy, outings, family gatherings, etc...
PT is coming in 4-5 times a week and OT is coming in 3 times a week. I am continuing to work on standing and squatting as well as reinforcing what the therapists are working on. David's mom is teaching him signing the alphabet (I need to learn too and he's catching on faster than me!)
I've got 2 good full-time caregivers now that I can depend on. Two have quit and 2 have gotten sick so at least once a week, I find myself high and dry without a caregiver. 
David's new doctor and therapists are really pressing for David to go to a state-of-the-art rehab facility. This is a point of prayer. We JUST got home and we've been away for FOURTEEN months! David is making progress, actually faster than at the last facility he was at. In some ways it's a tantalizing opportunity...in some ways it's totally not. We are asking God for wisdom and direction. To go or not to go, that is the question.
David just had a birthday yesterday.
My mom doesn't delete pictures off her camera for ages. So the other night, I was scanning through her camera and I found a picture of David from last birthday. Then I see proof of God's grace. Here is proof for you to see His goodness to us. I'll post the picture from a year ago when I get a chance. 
</p>
<p>10/17/13 (Rachel Hanson)
Today is one of those days of praise, a day when I'm so proud of my husband, and I am bursting with joy and thankfulness to the Lord!
David had an impromptu visit to his primary doctor to address a recent development of tremors in his right hand. He ended up reducing the gabapentin. Lately, I've been asking God for big things. We got big things today. We had two doctor's appointments today and I thought David would be wiped out. Instead, he had lots of energy. We were surrounded by God's people today: the van driver, the first doctor, and the PT. I was about to go shopping and was going to tell David good bye. He wanted to say something. I told him to try to voice it. To my surprise he not only said five words then, he kept going and going, word after word, whatever I asked him to say!! Then when I walked in after shopping, he almost shouted "I love you!"
Truly, God is doing great things!</p>

<p>10/25/13 (Rachel Hanson)
I can't say enough how great it is to have David communicating verbally! I don't know how to explain the difference of how it feels in my heart to have my husband TALK to me instead of laboriously spelling things out. I can actually look him in the eyes when we talk instead of staring at his thumb!!  It's like we've been sending telegrams to each other for over a year. We had the privilege of talking to my sister on the phone last night TOGETHER.  
Instead of going to another inpatient facility almost 2 hours away again, we may have the option of staying home and doing outpatient 10 minutes away. When I toured the OP facility, the OT I talked to said, "I know who you are!" I was a little puzzled how she knew me but her son is taught by David's dad and has been following our story! We are still praying about God's leading in this area. Since David is making such great gains at home, we'd like to stay home. I'm able to give him 2 pureed meals a day. He's loving the wholesome home-cooked meals and I'm loving cooking for us again!  We're hoping to wean him off the g-tube before the next visit to his "stomach doctor" in 3 months.
</p>
