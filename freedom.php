You have it written on every coin you keep in your pocket. It is inscribed on every dollar you have in your wallet. What does it mean to "trust in God" and why has such a statement held so much value for such a long time?

<p>Trust is a very difficult thing sometimes. Some have people they can trust most of the time, and some don't. However, any human we put full trust in lets us down at some point. When things like this happen it makes it more difficult to trust them or others in the future. The problem with puting our full trust in other men (whether they be, political leaders, kings, religious leaders, or any other person who may seem trustworthy, is that people are not without thier problems and failures (<b class="bible">Proverbs 14:12</b>). Centuries of improved technology where the newest, latest and greatest thing is always being advertised, and yet the world still seems to have problems. Why?  <b class="bible">Romans 3:23</b> tells us it is because of our sin. When we make wrong choices in life, consequences come. 

<p>
<b><u>SIN'S BONDAGE:</u></b>
<br /><b class="bible">Romans 6</b> tells us that all men have a master. They have someone or something that they commit their lives to and work towards. They dedicate thier time and energy towards that which they love. In Romans chapter 6, we see that men are either a slave to God, or a slave to sin. The difference is that God gives men the choice whether to choose to surrender thier lives to Him, or to choose sin. However, when sin gets hold of someone, it is like they are stuck in it, just as someone gets stuck in quicksand. It just never satisfies and keeps demanding more and more of the sinner, forcing him or her to sink deeper and deeper, until it's too late. How can we excape if we neglect His great salvation, to be free from sin?

