<center><h1><b>The Answer </b></h1></center>
       <p>Do you want to be saved? Well Jesus wants to save anyone who desires deliverance from sin. Do you want to be liberated from your sin? This involves repentance (<i>to turn around and go the opposite direction</i>). It means to be remorseful and sorry for what you've done and to welcome the help of God in making you into a new person. I would strongly urge you to follow the instruction found in Acts 3:19&nbsp;&nbsp;
       <span class="bible">Repent therefore and be converted, that your sins may be blotted out, so that times of refreshing may come from the presence of the Lord.&nbsp;&nbsp;</span>
       Why wait? Call on the name of Jesus and believe in His salvation! He's done everything that needs to be done. 2nd Corinthians 5:21;&nbsp;
       <span class="bible">For He made Him who knew no sin, to be sin for us; That we might become the righteousness of God in Him.&nbsp;&nbsp;</span>
       This is what the Bible describes as <u>justification</u> (a word that comes straight from the court room: <i>to declare righteous</i>).
       <br />Though a believer is not perfect he or she has an eternally righteous position before God. Christ's righteousness was put to their account.
       </p>
       <p>Receive His salvation before it's too late! Isaiah 55:6-7;&nbsp;
       <span class="bible">Seek the Lord while He may be found, call upon Him while He is near. Let the wicked forsake his way, and the unrighteous man his thoughts; Let him return to the Lord, and He will have mercy on him; and to our God, for He will abundantly pardon.</span>
       </p>
       <p> He is here right now. Believe in Him before you run out of time! Before your appointment with death comes upon you! If you trust in Jesus for your salvation, you're saved forever! He'll never let you go. Jn.6:37;&nbsp;
       <span class="bibleRed">All that the Father gives Me will come to Me, and the one who comes to Me, I will by no means cast out.&nbsp;&nbsp;</span>
       Jn.5:24;&nbsp;
       <span class="bibleRed">Most assuredly I say to you, he who hears My word and believes in Him who sent Me has everlasting life, and shall not come into judgment, but has passed from death into life.</span>
       <br />Is this promise true in your life? Have you passed from the judgment of your sin, which brings the second death, into the abundant life of Jesus?
