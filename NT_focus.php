<h2>Our Focus</h2>
<p><span class="lead">Christ is the gathering center.</span><br />
Although a local church is a gathering of like-minded believers, no fellowship based on people will work.  "<span class="bible">Truly our fellowship,</span>" said John, "<span class="bible">is with the Father, and with His Son Jesus Christ</span>" (<a href="http://www.biblegateway.com/cgi-bin/bible?passage=1+John+1:3&version=NKJV" target="_blank" class="plainlink">1 Jn. 1:3</a>).  <a href="http://www.biblegateway.com/cgi-bin/bible?passage=Matt+18:20&version=NKJV" target="_blank" class="plainlink">Matthew 18:20</a> states: "<span class="bible">For where two or three are gathered together in MY name, there am I in the midst of them.</span>"<br />
It is so easy to be distracted from Christ, to find ourselves gathering to a preacher, a set of doctrines, a sacrament, or form of church government.  This is very different headship (<a href="http://www.biblegateway.com/cgi-bin/bible?passage=Col+1:18&version=NKJV" target="_blank" class="plainlink">Col. 1:18</a>) in our assembly practically.  We believe He deserves this, for it is His church: "<span class="bible">Christ also loved the church, and gave Himself for it</span>" (<a href="http://www.biblegateway.com/cgi-bin/bible?passage=Eph+5:25&version=NKJV" target="_blank" class="plainlink">Eph. 5:25</a>).</p>

<p><span class="lead">The Lord's Supper occupies a central place.</span><br />
As the early Christians did, we come together on the first day of each week to "<span class="bible">break bread</span>" (<a href="http://www.biblegateway.com/cgi-bin/bible?passage=Acts+2:42;20:7&version=NKJV" target="_blank" class="plainlink">Acts 2:42; 20:7</a>).  Being a divine appointment, it should never be relegated to a secondary place, or treated as a matter of occasional obedience.  It is to be a time of calling our Lord to mind and declaring His death for us.  It is observed not as a sacrament administered by a clergyman, but celebrated as described in <a href="http://www.biblegateway.com/cgi-bin/bible?passage=1+Cor+11&version=NKJV" target="_blank" class="plainlink">1 Corinthians 11</a>.  The prime purpose of this time is not to minister to the saints, as in other meetings of the church, but to minister to the heart of God through appropriate reading of scriptures, worshipful hymns, and prayers expressing to God the worthiness of our Saviour.</p>


