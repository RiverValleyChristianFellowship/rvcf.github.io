<center><h1><b>An Appointment with Death</b></h1></center>
    <p>Hebrews 9:27 says,&nbsp;
    <span class="bible">And as it is appointed unto man to die once, but after this the judgement.&nbsp;&nbsp;</span>
    Have you ever thought seriously about this statement? We have an <i>appointment</i> with death. And for the most part, we have no idea when that will be! And then what?&nbsp;&nbsp;&nbsp;
    <span class="bible">After this comes judgment.</span>&nbsp;&nbsp;
    Are you ready to be judged by God's perfect standard for how you lived your life? Think about it. Be honest with yourself.
    <br />The future was revealed to John in Revelation 20:11-15;&nbsp;
    <span class="bible">"Then I saw a great white throne and Him who sat on it, from whose face the earth and the heaven fled away. And there was found no place for them. And I saw the dead, small and great, standing before God, and books were opened. And another book was opened, which is the Book of Life. And the dead were judged according to their works, by the things which were written in the books. The sea gave up the dead who were in it, and Death and Hades delivered up the dead who were in them. And they were judged, each one according to his works. Then Death and Hades were cast into the lake of fire. This is the second death. And anyone not found written in the Book of Life was cast into the lake of fire."&nbsp;&nbsp;</span>
    </p>
    <p>Is your name written in the Book of Life, or are you going to have your part in the second death? The second death will be far worse than physical death, because it will be eternal torment of the soul and separation from God. There will be a lot of people who will be astonished at the consequences for not seeking answers to eternal questions; questions they took far too lightly. Are you ready when your time comes?
    </p>
