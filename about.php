<h1>IS IT POSSIBLE TO MEET AS A NEW TESTAMENT CHURCH TODAY?</h1><br />
<h2>A word of explanation</h2><br />

<p><span class="lead">Have you ever wondered what it would have been like to meet with the first Christians?</span><br />
One of the greatest blessings this side of heaven is to gather with others who take God and His Word seriously.  We are a group of believers saved by grace though faith in Christ and His finished work <a href="http://www.biblegateway.com/cgi-bin/bible?passage=Ephesians+2:8-9&version=NKJV" target="_blank" class="plainlink">(Eph. 2:8-9)</a> who meet simply around the Lord Jesus, seeking to follow only the directions given in the New Testament.  We believe that the church is God's idea.  Christians do not meet in congregations merely because it is desirable or helpful, but because the Lord has ordained it <a href="http://www.biblegateway.com/cgi-bin/bible?passage=1+Thessalonians+2:14&version=NKJV" target="_blank" class="plainlink">(1 Thess. 2:14)</a>.</p>
<p>Our Lord Himself first spoke of the church when He declared that He was its builder and would establish it on a sure foundation <a href="http://www.biblegateway.com/cgi-bin/bible?passage=Matthew+16:18&version=NKJV" target="_blank" class="plainlink">(Mt. 16:18)</a>.  He also first spoke concerning the local gatherings of believers <a href="http://www.biblegateway.com/cgi-bin/bible?passage=Matthew+18:17&version=NKJV" target="_blank" class="plainlink">(Mt. 18:17)</a>.</p>
<p>There may be some things about a local church like this that would be different to you.  We would appreciate a few moments to explain how we meet and why.</p>
<p><span class="lead">By the way, there are many who meet like this.</span><br />
You may have heard of names like George Muller, the great man of faith; H.A. Ironside, the famous Bible expositor; Jim Elliot, the intrepid martyr of Ecaudor; or W.E. Vine, author of the well-known <i>Dictionary of Expository Words</i>.  These met simply according to the pattern found in the New Testament, as thousands do worldwide.  There is no division between "clergy" and "laity" in our meetings, because there is no such division in the New Testament.  We want to gather simply as Christ's ones in His name,<sup><a href="#endnote">1</a></sup> recognizing no names that would divide God's people.<sup><a href="#endnote">2</a></sup>  To be gathered in His name is to meet by His authority, submitting to His Lordship, and following His Word.</p>

