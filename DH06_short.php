<center><h2><b>Live Once ~ Die Twice<br />
or<br />
Live Twice ~ Die Once</b></h2></center>
<p>
<ul type="circle">
    <li>Of all the questions people ask themselves, one of the most important is, "What happens when we die?" If we don't open up and seek God's answers, it will make an eternity of difference.</li>
    <li>The first thing we need to realize is that we're all sinners. In Jeremiah 17:9 God tells us that our hearts are deceitful above all things and desperately wicked. If our hearts have us tricked, the fact still remains. Romans 3 lets us know that we've messed up big time before the holy and righteous God.</li>
    <li>Revelation 20 tells us about the consequence for this. There is a place called the Lake of Fire where guilty sinners will be judged for their sin. When God's wrath is revealed upon sin in this place, the smoke of torment ascends forever and ever.</li>
    <li>But God doesn't want us to go there! He wants us to be in heaven with Him after our physical death! 1st Timothy 2:4 teaches that <span class="bible">"God desires all men to be saved!"</span><br />
 In John 10:10 Jesus said, "<span class="bibleRed">I have come that they might have life, and that they might have it more abundantly!</span>"<br />
 Jesus Christ came into this world to take His Father's wrath towards sin upon Himself while He suffered and gave up His life on a Roman cross. Even though Jesus was one with God, perfect and holy, He died so that we wouldn't have to die.<br />
 2nd Corinthians 5:21 says, "<span class="bible">For He made Him Who knew no sin, to be sin for us; that we might become the righteousness of God in Him.</span>" Mankind has the opportunity to be declared righteous! God wants to give us eternal life for free!</li>
    <li>Receiving this life however, doesn't make any sense unless we understand what we must be saved from! Altogether mankind is corrupt. So in essence, we need to be saved from what we are. Our sins are what brought God's wrath upon His Son.<br />
 When the Lord saves us, He wants to make us new (2nd Corinthians 5:17). Part of believing in Jesus is to seek God's help in changing you. The Bible describes this as repentance (to turn the other way). In Acts 3:19 God's word says, "<span class="bible">Repent therefore, and be converted, that your sins may be blotted out and that times of refreshing may come from the presence of the Lord!</span>"</li>
    <li>Part of repentance is to realize there's no work you can do to be saved. Titus 3:5 says, "<span class="bible">not by works of righteousness which we have done, but according to His mercy He saved us!</span>"  We have to give up on ourselves, and give in to God.<br />
 When God saves us, we're saved forever. In John 6:37 Jesus says, "<span class="bibleRed">All that the Father gives Me will come to Me, and the one who comes to Me I will by no means cast out!</span>" Next, He wants to start a good work in us!</li>
</ul>
 Now you've read the message of good news! Are you going to live once and die twice or die once and live twice??<br />

