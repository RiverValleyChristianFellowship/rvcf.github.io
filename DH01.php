<center><h1><b>Hard Questions</b></h1></center>
        <p>
        There are several questions people ask themselves throughout their lives. Sadly, the most important questions are often neglected or forgotten.&nbsp;&nbsp;
        <i>Is there a God? What is my existence all about? How did everything we see come together?&nbsp;&nbsp;</i>
        Some people avoid these questions because they are scared or frightened off by potential answers.&nbsp;&nbsp;
        <i>Why do we experience pain and suffering? Why do people hate or have disagreements? Why do I feel guilty?&nbsp;&nbsp;</i>
        It may be hard, but these matters are crucial to face up to! These Spiritual questions are far more important than anything in this world. How about this one:&nbsp;
        <i><b>Where am I going when I die?</b></i>
        </p>
        <p>
         This is serious stuff. The alarming thing is, Satan has too many people wrapped around his little finger because of materialism. In Mark 8:36-37 the Bible says,&nbsp;&nbsp;
         <span class="bible">For what will it profit a man if he gains the whole world, and loses his own soul? Or what will a man give in exchange for his soul?&nbsp;&nbsp;</span>
         We are so distracted by things of this world, that we far too often fail to consider the fact that we could die at any time! We would do well to never take one breath for granted. In Psalm 103:15-16, God's Word says:&nbsp;&nbsp;
         <span class="bible">As for man, his days are like grass; as a flower of the field, so he flourishes. For the wind passes over it, and it is gone, and its place remembers it no more.&nbsp;&nbsp;</span>
         Also, Isaiah 40:6-8 says:&nbsp;
         <span class="bible">The voice said, 'Cry out!' And he said, 'What shall I cry?' All flesh is grass, and all its loveliness is like the flower of the field. The grass withers, the flower fades, because the breath of the Lord blows upon it; Surely the people are grass. The grass withers, the flower fades, but the word of our God stands forever.</span>
         </p>
         <p>
         Peter, a disciple of Jesus Christ, quotes something similar in 1st Peter 1:24-25;&nbsp;&nbsp;
         <span class="bible">All flesh is as grass, and all the glory of man as the flower of the grass. The grass withers, and its flower falls away, but the word of the Lord endures forever.</span>
         <br />We see these things happening every day. People get old, become frail, and die. Things happen such as sickness, murder, car accidents, wars, terrorism,…etc. Some people die young and others old. We don't like to admit it, but physically we are very vulnerable.
         <br />There are a lot of people who would be smart to stop worrying about things in this world and start worrying about spiritual things.
         </p>
