<p>12/12/13 (Rachel Hanson) We got our modified van this morning!!! It will be so nice to not have to do hard transfers anymore and David and I will have the freedom to go on dates out by ourselves!!! Thank you so much for your specific prayers for this. It's such a huge blessing!</p>


<p> 12/10/13 (Janet Rawlings) "HOME" the word that has taken on a new meaning to David, Rachel and Jocelyn. It has taken on a new sweetness for me, too. I am no longer running up and down the freeways and packing up a suitcase so that I could be a help to David and Rachel. It has taken on a new meaning for David's family, too. To have David, Rachel and Jocelyn be with them for Thanksgiving was, I am sure, a very special day. What joy for us to be packing up to go visit them in their new home as parents and grandparents not caregivers.
It has and is an amazing journey to go on. We (Rawlings and Hanson families) have felt borne up on eagles wings. Many of you have told me that Rachel is an amazing woman and that she encourages them in their walk with the Lord as they read her posts. They, quite often, like to tell me that I did a good job raising her, but I want to set the record straight - It has been only the Lord bearing her up and speaking His Words into her heart. We, as parents, give God all the glory. His NAME be praised!!! Very important in this journey have been the prayers of thousands of people. We want to thank you VERY much,
because it has made a huge difference. It always brings me to tears when someone will come up to me and tell me they are STILL praying for David and Rachel. It has been 16 months since David's accident.
16 months of the most amazing gospel outreach ever created. Nobody would have thought of it, because it is so painful and nobody would have chosen it. Never the less, God has chosen this way of getting his Good News out to people who would normally not get to hear about the Savior who loves them so much and died for them and is in heaven waiting to fold them in His arms. Amazing things keep happening so that David's CD's keep getting handed out. To make a long story short, last week a car load of young people stopped at our house to buy almonds. In the course of the conversation I told them about David's accident and ended up giving them a CD and they promised to listen to it. These young people were from Oakland, CA going to Oregon. How unlikely is that?!
After being with David and Rachel in their new home for 6 weeks I went to say good-bye to David and he got tears in his eyes. Wow! A son-inlaw that actually was going to miss his mother-inlaw!!! What a sweet young man he is. And how he displays the heart of Christ. We can't measure what is being done for eternity in the hearts of men and women on this journey that David and Rachel are taking. The struggles are still sometimes overwhelming for David and Rachel, but if you learn more of who Jesus Christ is through their lives and the scriptures that Rachel posts then the fruit will be displayed in your life, too,
and make it worth while for David and Rachel to have gone through this valley.
We praise God for allowing David, Rachel and Jocelyn to be at HOME!</p>

<p> 12/20/13 (Rachel Hanson) As David continues to progress, he is entering a cognitive stage that is challenging for me, especially. I won't go into a lot of detail because I want to respect him and his privacy. When David was progressing through the first stages of the Rancho Los Amigos cognitive scale, we were thankful he seemed to skip the 4th one, as that stage is associated with anger and combativeness. But I'm wishing he'd skip this stage too!  So please pray for us.

The neurologist has outlined a plan to take gabapentin out of his medicine list by next weekend. Please pray that this change does not cause a seizure. I've noticed he is more stiff lately but talking more spontaneously and louder. It's a balancing act! David has been practicing different tones and more consonant sounds in speech therapy. If I don't go to therapy with him for a couple days, I'm always impressed by what he's doing in speech the next time I go with him! 

We are putting our new van to work and have it out 6-8 times a week!

These verses have encouraged me: Psalms 66:8-20
Oh, bless our God, you peoples! And make the voice of His praise to be heard, Who keeps our soul among the living, And does not allow our feet to be moved. For You, O God, have tested us; You have refined us as silver is refined. You brought us into the net; You laid affliction on our backs... We went through fire and through water; But You brought us out to rich fulfillment. I will go into Your house...I will pay You my vows, Which my lips have uttered And my mouth has spoken when I was in trouble...Come and hear, all you who fear God, And I will declare what He has done for my soul. I cried to Him with my mouth, And He was extolled with my tongue. If I regard iniquity in my heart, The Lord will not hear. But certainly God has heard me; He has attended to the voice of my prayer. Blessed be God, Who has not turned away my prayer, Nor His mercy from me! (Psalms 66:8-20 NKJV)</p>

