<center><h1><b>Sinful, Corrupt, and Unrighteous before God </b></h1></center>
      <p>If you want to be part of the Lamb's Book of Life, there is something you'll have to understand. There's nothing you can do to earn eternal life! Without Jesus in our lives, the entire human race is nothing but a bunch of wretched sinners. Some may try to cover it on the outside, but God sees your heart. 1st Samuel 16:7 says,&nbsp;&nbsp;
      <span class="bible">The Lord does not see as man sees; for man looks at the outward appearance, but the Lord looks at the heart.&nbsp;&nbsp;</span>
      And what does He see when He looks at our heart? Jeremiah 17:9;&nbsp;
      <span class="bible">The heart is deceitful above all things, and desperately wicked. Who can know it?&nbsp;&nbsp;</span>
      We don't even know how bad we are, because we're so deceitful! We even deceive ourselves! Romans 3:10-18;&nbsp;
      <span class="bible">There is none righteous, no, not one; There is none who understands; There is none who seeks after God. They have all turned aside; They have together become unprofitable; There is none who does good, no, not one. Their throat is an open tomb; With their tongues they have practiced deceit; The poison of asps is under their lips; Whose mouth is full of cursing and bitterness. Their feet are swift to shed blood; Destruction and misery are in their ways; And the way of peace they have not known. There is no fear of God before their eyes.&nbsp;&nbsp;</span>
      </p>
      <p>Humanly speaking, we are without hope. Even the good things a sinner does are nothing but filthy rags to God! Isaiah 64:6-7;&nbsp;
      <span class="bible">But we are all like an unclean thing, and all our righteousnesses are like filthy rags; We all fade as a leaf, and our iniquities, like the wind, have taken us away.&nbsp;&nbsp;</span>
      Here we see that our sins will cause us, like the wind to a leaf, to be carried away. James 2:10 says,&nbsp;&nbsp;
      <span class="bible">If a man keeps the whole law, and yet stumbles in one point, he is guilty of all.&nbsp;&nbsp;</span>
      There is nothing good we can do to earn favor with God. Mankind desperately needs to realize this! Ezekiel 33:12;&nbsp;
      <span class="bible">The righteousness of the righteous man shall not deliver him in the day of his transgression; as for the wickedness of the wicked, he shall not fall because of it in the day that he turns from his wickedness; nor shall the righteous be able to live because of his righteousness in the day that he sins.&nbsp;&nbsp;</span>
      And Romans 3:23;&nbsp;
      <span class="bible"><u>All</u> have sinned, and fallen short of the glory of God.</span>
      </p>
